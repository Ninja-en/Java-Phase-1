public class MathOperator {
    public static void main(String[] args) {
        int a=10;
        int b=5;

        System.out.println("ผลบวก = "+(a+b));
        System.out.println("ผลบวก = "+(a-b));
        System.out.println("ผลบวก = "+(a*b));
        System.out.println("ผลบวก = "+(a/b));
        System.out.println("ผลบวก = "+(a%b));

    }
    
}
